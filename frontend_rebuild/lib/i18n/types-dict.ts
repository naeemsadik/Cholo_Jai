// Use `import type { Dictionary } from "./types-dict"; ... as const;` to
// infer the literal type for each dictionary.
//
// Why hand-rolled and not a library:
//   - The whole dictionary is ~3KB gzipped, way under a library's overhead.
//   - We need variable interpolation ({n}, {year}) which most i18n libs
//     solve in 30 lines of code anyway.
//   - The `as const` + literal-type pattern gives us compile-time
//     completeness checks: add a key to en, you MUST add it to bn.

